; SIMPLE HELLO PROGRAM.
SCREEN = $D0A8  ; START SCREEN ADDRESS FOR THE MESSAGE

.ORG $0220
    .BYTE $22, $02 ; PREFACE CODE WITH START ADDRESS $0222

START:
    LDX #$00
LOOP:
    LDA MESSAGE, X
    BEQ DONE
    STA SCREEN, X
    INX
    JMP LOOP
DONE:
    RTS
    BRK

MESSAGE:
    .BYTE "HELLO"
    .BYTE $00

